      <h2>Making meetings more interactive and insightful.</h2>
      <h3>What is twitvote and how does it help me?</h3>
      <p>Ever wanted to do an 'Ask the Audience', like the quiz show 'Who wants to be a Millionaire?'.  twitvote lets you do exactly that but without the need
         to rent expensive handsets and computer systems.  twitvote is fast and free.  </p>
      <p>twitvote can be used by thousands of people at the same time, including where
         people are not physically present; for example if you are running a webinar or phone conference you can use twitvote to ask questions 
         to your audience and get their answers and opinions. </p> 
      <p>Your audience can vote using text message, twitter or via the web site; so they do not even need to be at their computers.</p> 
      <h2>How does twitvote work?</h2>
      <p>Using twitvote is a simple two step process.  Step 1 - create a question for people to vote on.  Step 2 - people vote.</p>
      <h3>Step 1 - How do I set up a question?</H3>
      <p>Just click on 'Create Question' (either the button or the menu option in the top left of this page) and type in your question.  When you've finished your question, click the 'Save Question and Start Voting' 
          button at the bottom of the question form and your audience can start voting.  Takes less than a minute!</p>
      <h3>Step 2 - How do people vote?</H3>
      <p>People can vote by sending a message using: <ul>
        <li> twitter </li>
        <li> online (click 'Vote Now' above) </li>
        <li> by text message (to +44 7786 200 690) </li>
      </ul></p>
      <p>The message always has the same format:</p>
      <p><em> &nbsp &nbsp twitvote &nbsp &nbsp question_code &nbsp &nbsp answer_number</em> </p> 
      <p>where:</p>
      <ul>
      <li><em>twitvote</em> is the word twitvote.  This word is used to identify which messages relate to twitvote and which do not. </li>
      <li><em>question_code</em> is a unique code that you make up when creating a question.  The code allows twitvote to work out which question people are voting on.</li>
      <li><em>answer_number</em> is the answer number from the list of possible answers that you type in when creating a question.</li></ul>
      <h3>Show me an example</H3>
      <p>Suppose we have the following question:</p>
      <p>Question: What is your favourite day of the week?</p>
      <p>Question Code: myfavday</p> 
      <p>Possible answers</p>
       <ul>
        <li> 1 = Monday </li>
        <li> 2 = Tuesday </li>
        <li> 3 = Wednesday </li>
        <li> 4 = Thursday</li>
        <li> 5 = Friday</li>
        <li> 6 = Saturday</li>
        <li> 7 = Sunday</li>
      </ul></p>
      <p>To vote for Tuesday, you could text / tweet / vote online (using Vote Now above) the following message:</p>
       <ul>
        <li> twitvote myfavday 2 </li>
      </ul></p>
      <h2>Tips and Tricks</h2>
      <h3>How do Question Codes Work?</h3>
      <p>To allow as many people as possible use of twitvote, question codes are allocated on a first come first served basis and last for one day.
         There is no need to reserve a question code, just use it and the system will let you know if there is a problem.  Clearly, the more relevant
         a code is to you and your event, the less likely it is that there will be a clash.  The question code 'day' should not be used except
         to test how the system works.</p>
         
      <h3>How do I change a Question once I have created it?</h3>
      <p>If you create a question and want to update it (for example if you misspell a word or want to add a new answer etc.), just go back to the 
         Create Question page and make the changes.  The fastest way to do this is using your browser's back button.  When you press the 
         Save Question and Start Voting button, twitvote will update the question with the changes you have made.</p>   
         
      <h3>How do I set up multiple questions?</h3>
      <p>If you have multiple questions to set up in advance, twitvote is designed to do this but you MUST do it as follows:</p>   
       <ul>
        <li>Set up each question in a separate browser - DO NOT use multiple tabs in the same browser.</li>
        <li>We recommend using similar codes for each question.  For example if you are running an event like Digital Leaders and have a twitter
        hash tag of #DigiLead, then use codes like #DigiLead01 for the first question, #DigiLead02 for the second question etc.  For people unfamiliar
        with twitter, you do not need to use a #.  Any code made up of letters and numbers will work.</li>
      </ul>
         
      <h3>How do I send people the results?</h3>
      <p>Distributing the results is easy.  You can include your question's URL into emails and presentations.  For example, if we 
         ran the <em>'Favourite day of the week'</em> example above on the 18th November 2012, the following URL would give the results:</p>
         
      <p><b>http://www.twitvote.co.uk/results.php?vote_code=myfavday&vote_date=2012-11-18</b></p>
      
      <p>You can see in this example that the vote_code is myfavday and the vote_date is 2012-11-18.  By changing these two values you can get the 
         results of any question on any day.  If changing these seems complicated, you can easily copy the URL at the same time people are voting.
         It is displayed at the top of the Results page for every question that you ask.</p>   
       
      <h3>Can people vote more than once?</h3>
      <p>Currently we do allow people to vote more than once.  This feature supports people who need to share mobile phones and computers etc.   
         We have also come across a small (and ever shrinking) number of venues where there is no mobile signal and no wireless internet.  In these cases, we use paper forms and then
         add the answers to the system later for distribution.  As people
          use twitvote and we get feedback, we will decide whether to keep or change this feature.</p>   
         
